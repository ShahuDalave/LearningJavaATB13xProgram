package Lab01_Literals;

public class Ex01_Literals {
    public static void main(String[] args) {
        int age = 65; // 65-->> literal
        char c1 = 'A';

        // Escape Characters
        char new_line = '\n';
        char tab_line = '\t';
        char back_space = '\b';
        char carriage_return = '\r';

        System.out.println("ShahuDalave");
        System.out.println("Shahu" + new_line + "Dalave");
        System.out.println("shahu" + tab_line + "Dalave");
        System.out.println("Shahu" + back_space + "Dalave");
        System.out.println("Shahu" + carriage_return + "Dalave");

        ;


    }
}
